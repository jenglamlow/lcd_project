static void spi_open(void)
{
    /* Initialise SSIO peripheral */
    SysCtlPeripheralEnable(SYSCTL_PERIPH_SSI0);
}


/*-----------------------------------------------------------------------------
 *  Initialisation
 *-----------------------------------------------------------------------------*/
void spi_init(spi_services_t *spi_services)
{
   spi_services->open = spi_open;
}

